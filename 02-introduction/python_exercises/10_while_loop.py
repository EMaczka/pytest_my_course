# Create while loop that will print result 5 times
i = 0
while i < 5:
    i += 1
    print("This is first result")

# Create infinite while loop that will print result (to stop it just click stop button)
while True:
    print("This is second result")

# Create while loop that will use user input as termination
while True:
    print("This is third result")
    i = input("Enter q to exit ")
    if i == "q":
        exit()
